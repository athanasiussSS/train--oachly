// Утилиты для валидации форм

// validateEmail(email) - валидация email
//   - Проверка формата email
//   - Возвращает boolean

// validatePhone(phone) - валидация телефона
//   - Проверка формата телефона (российский формат)
//   - Возвращает boolean

// validateEmailOrPhone(value) - валидация email или телефона
//   - Определяет что введено (email или телефон)
//   - Вызывает соответствующую валидацию
//   - Возвращает boolean

// validatePassword(password) - валидация пароля
//   - Проверка требований: минимум символов, заглавные, строчные, цифры, спецсимволы
//   - Возвращает объект с детальной информацией о требованиях
//   - Возвращает { isValid: boolean, requirements: PasswordRequirements }

// validateVerificationCode(code) - валидация кода подтверждения
//   - Проверка что код состоит из 6 цифр
//   - Возвращает boolean

// validateName(name) - валидация имени
